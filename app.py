import streamlit as st
import google.generativeai as genai
from PIL import Image

# 1. ตั้งค่าหน้าเว็บหน้าตาคลีนๆ
st.set_page_config(page_title="TWI Analyzer - พี่โจ รีเจนตามงบ", page_icon="📊", layout="centered")

st.title("📊 ระบบสแกนสินค้าทำเงิน TWI Scorecard")
st.subheader("โดย ช่องพี่โจ รีเจนตามงบ")
st.write("คัดสินค้าก่อนลงแรง ด้วยดาต้าหลังบ้าน TikTok Shop 7 วัน (ระบบใช้งานฟรี 100%)")
st.write("---")

# 2. ดึง API Key อย่างปลอดภัยจากระบบหลังบ้าน (Secrets)
try:
    api_key = st.secrets["GEMINI_API_KEY"]
    genai.configure(api_key=api_key)
except Exception as e:
    st.error("⚠️ ไม่พบ API Key ในระบบหลังบ้าน กรุณาตั้งค่า Secrets บน Streamlit Cloud ก่อนครับ!")
    st.stop()

# 3. ช่องอัปโหลดรูปภาพ
uploaded_file = st.file_uploader("📸 แคปหน้าจอแดชบอร์ดสินค้า 7 วัน แล้วอัปโหลดที่นี่", type=["jpg", "jpeg", "png"])

if uploaded_file is not None:
    image = Image.open(uploaded_file)
    st.image(image, caption='รูปภาพแดชบอร์ดที่อัปโหลดเข้าสู่ระบบ', use_container_width=True)
    
    # 4. ปุ่มกดเริ่มสั่งการ AI
    if st.button("🚀 เริ่มสแกนและวิเคราะห์ผลด่วน"):
        with st.spinner('เลขาจีกำลังใช้ AI สแกนรูปภาพและคำนวณตามสูตร TWI สักครู่ครับ...'):
            try:
                model = genai.GenerativeModel('gemini-1.5-flash')
                
                # ร่างคำสั่งลอจิก TWI ฝังในหัวบอท
                prompt = """
                คุณคือ บอทวิเคราะห์สินค้าทำเงิน TWI (TikTok Winner Index) ประจำช่อง พี่โจ รีเจนตามงบ
                งานของคุณคือ อ่านรูปภาพแคปหน้าจอข้อมูลหลังบ้าน TikTok Shop 7 วัน ดึงตัวเลขออกมาและคำนวณคะแนนรวม TWI (เต็ม 100 คะแนน) ตามเกณฑ์ดังนี้:
                1. คะแนน CTR (เต็ม 30): ถ้า CTR < 2% ได้ 0 | 2%-4% ได้ 15 | > 4% ได้ 30
                2. คะแนนปิดการขาย (เต็ม 40): คิดจาก (คำสั่งซื้อ / เพิ่มลงรถเข็น) x 100 -> ผลลัพธ์ < 10% ได้ 0 | 10%-25% ได้ 20 | > 25% ได้ 40
                3. คะแนนส่วนแบ่งตลาด (เต็ม 30): คิดจาก คำสั่งซื้อ / จำนวนครีเอเตอร์ -> ผลลัพธ์ < 2 ได้ 0 | 2-10 ได้ 15 | > 10 ได้ 30

                ให้ตอบกลับด้วยโครงสร้างนี้เสมอด้วยภาษาที่เป็นกันเอง กระชับ ดุดัน สไตล์พี่โจ รีเจนตามงบ:
                ---
                📊 สรุปดาต้าที่อ่านได้จากรูป:
                - คำสั่งซื้อ: [ตัวเลข] ออเดอร์
                - จำนวนครีเอเตอร์: [ตัวเลข] คน
                - CTR: [ตัวเลข]%
                - เพิ่มลงรถเข็น: [ตัวเลข] ครั้ง

                🧮 ผลการคำนวณคะแนน TWI:
                - คะแนน CTR: [คะแนน]/30
                - คะแนนปิดการขาย: [คะแนน]/40
                - คะแนนตลาด: [คะแนน]/30
                ** รวมคะแนน TWI = [คะแนนรวม]/100 คะแนน **

                🚨 บทวิเคราะห์สรุปจากพี่โจ:
                [ถ้าได้ 80-100: 🟢 ลุยสุดตัว (Winner)! สินค้าตัวนี้โคตรคุ้มเหนื่อย ปิดการขายง่าย อัดคลิปถล่มได้เลย]
                [ถ้าได้ 50-79: 🟡 ลองเชิงก่อน! พอทำเงินได้ เข้าสูตรเทส 9 คลิปของพี่โจก่อน ถ้าไข่แตกค่อยสเกล]
                [ถ้าได้ ต่ำกว่า 50: 🔴 ปัดตกทันที! อย่าหาทำ เสี่ยงเหนื่อยฟรี ตลาดตันหรือขายยากเกินไป]
                ---
                """
                
                # ส่งรูป + คำสั่งไปให้บอทคำนวณ
                response = model.generate_content([prompt, image])
                
                st.success("✨ วิเคราะห์เสร็จเรียบร้อย!")
                st.markdown(response.text)
                
            except Exception as e:
                st.error(f"เกิดข้อผิดพลาดในการประมวลผล: {e}")